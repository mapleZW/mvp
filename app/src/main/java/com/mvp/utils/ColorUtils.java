    public static final int[] pickColor(int length) {

        Random random = new Random();
        int[] values = new int[length];
        HashSet<Integer> hashSet = new HashSet<>();

        // 生成随机数字并存入HashSet
        while (hashSet.size() < values.length) {
            hashSet.add(random.nextInt(COLORS.length));
        }
        hashSet.toArray();

        // 遍历数组并打印数据
        for (int i = 0; i < values.length; i++) {
            values[i] = COLORS[(int) hashSet.toArray()[i]];
            System.out.println(COLORS[(int) hashSet.toArray()[i]]);
        }
        return values;
    }

